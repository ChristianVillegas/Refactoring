
public class Lloguer {
	private int data;
	private int dies;
	
	public Lloguer(int data, int dies) {
		this.data = data;
		this.dies = dies;
	}
	
	public int getData()		{ return data;	}
	public int getDies()		{ return dies;  }
	
	public void setData(int data) { this.data = data; }
	public void setDies(int dies) { this.dies = dies; }

}
